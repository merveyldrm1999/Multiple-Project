function ucgenAlani() {
    let birinciDeger = document.getElementById("birinci-deger").value;
    let ikinciiDeger = document.getElementById("ikinci-deger").value;
    let sonuc = 0;

    sonuc = parseInt(birinciDeger) * parseInt(ikinciiDeger) / 2;
    document.getElementById("sonuc").innerHTML = sonuc;








}